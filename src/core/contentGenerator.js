function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)]
}

function buildTagString(primaryEmotion) {
  const pool = POOLS[primaryEmotion] || POOLS.reflective
  const a = pick(pool.adj)
  const v = pick(pool.verb)
  const im = pick(pool.image)
  return `${a}${v}的${im}`
}

function buildTagGrief() {
  return `${pick(GRIEF_TAG.adj)}${pick(GRIEF_TAG.verb)}的${pick(GRIEF_TAG.image)}`
}

function buildMetaphorString() {
  if (Math.random() < 0.55) {
    const h = pick(METAPHOR_HEAD)
    const p = pick(METAPHOR_PLACE)
    const t = pick(METAPHOR_TAIL)
    return `${h}一间${p}，${t}`
  }
  return `情绪像${pick(META_ALT.thing)}，${pick(META_ALT.state)}`
}

/**
 * @param {string} primaryEmotion
 * @param {string} [contextType]
 */
export function generateTagAndMetaphor(primaryEmotion, contextType) {
  const tag = uniqueLine(
    'tag',
    () => (contextType === 'grief' ? buildTagGrief() : buildTagString(primaryEmotion)),
    80
  )
  const metaphor = uniqueLine(
    'metaphor',
    () => (contextType === 'grief' ? pick(GRIEF_METAPHORS) : buildMetaphorString()),
    56
  )
  return { tag, metaphor }
}
