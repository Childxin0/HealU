function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)]
}

/**
 * @param {string} contextType
 * @param {string} primaryEmotion
 * @returns {[string, string, string]} 极低成本 / 环境 / 认知
 */
export function generateBufferingActions(contextType, primaryEmotion) {
  void primaryEmotion
  const k = poolKey(contextType)
  const pool = POOLS[k] || POOLS.general

  const micro = uniqueLine('action', () => pick(pool.micro), 56)
  const env = uniqueLine('action', () => pick(pool.env), 56)
  const cog = uniqueLine('action', () => pick(pool.cog), 56)
  return [micro, env, cog]
}

